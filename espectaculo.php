            <h1 class="red-bold">El Espectáculo</h1>
            <p>
                Manolete es arte, es tauromaquia y es cultura, pero ante todo, además de ser un
                símbolo del S.XX, nuestro protagonista fue un ejemplo de disrupción, de cómo una
                personalidad adelantada a su época se convierte en un símbolo internacional de
                libertad y autodeterminación.
                <br/><br/>
                El amor de su vida, Lupe Sino, así como la relación con su madre y una vida
                apasionada en todos los sentidos, envuelta en “ glamour” y lujo (en contraste con la
                España de los años 40) relata la vida del protagonista, destacando escenarios tan
                famosos en la época como Chicote y Lhardy.
                <br/><br/>
                Con “Espectáculo Musical Manolete”, el primero, a nivel mundial, basado en la
                vida de un torero, el público va a disfrutar de tres actos escritos en castellano, en los
                cuales el flamenco se funde con la lírica, y con la danza, haciendo de éste, un
                <span class="red-bold">espectáculo único e innovador</span>. Pero éstas dos horas van mucho más allá, ya
                que el
                musical sale del escenario para “tomar” la zona VIP y el graderío, <span class="red-bold">haciendo
                    partícipes del ambiente de la época a todos los asistentes</span>.
                <br/><br/>

                <i class="cursive-sub-red">Posicionamiento:</i>
                <br/>
                Cuando se observa el nivel de satisfacción del consumidor (ya consumado el
                acto), se defiende que, cuando el gasto se destina al bienestar propio, no duele, sino que
                más bien no necesita ser justificado más allá del placer, entendido como conjunto de
                sensaciones que mejoran el bienestar.
                <br/><br/>
                Si a la observación anterior se le agrega que los productos musicales contribuyen
                de forma directa a la satisfacción de necesidades existenciales y axiológicas,
                <span class="red-bold"> se concluye estimando en muy elevado el beneficio que aporta Espectáculo
                    Musical Manolete a sus consumidores</span>.
                <br/><br/>
                Cuando además se reflexiona acerca de que la asistencia a estos espectáculos es
                un elemento diferenciador entre los estratos sociales, se deduce que comprar “Espectáculo Musical
                Manolete” no solo aporta bienestar interior (satisfacción,
                sensaciones, etc...) sino que supone adquirir un estatus exclusivo, supone pertenecer a
                un grupo.
                <br/><br/>
                Manolete va encaminado a satisfacer las necesidades relativas de quienes
                compran la entrada. Esas necesidades que posicionan a los espectadores por encima de
                los demás y aportan una sensación se distinción (en definitiva, satisfacción) frente a los
                que “no pueden” ir a verlo (sea por el motivo que sea...).
                <br/><br/>
                <i class="cursive-red">La Ventaja Competitiva</i>
                <br/>
                Manolete va a emplear una estrategia mixta de enfoque y diferenciación, para
                lograr obtener así su ventaja competitiva, y con ella el posicionamiento deseado en el
                mercado.
                <br/><br/>
                Espectáculo Musical Manolete es una combinación de géneros de culto como la
                lírica, el flamenco y la danza con las tecnologías más punteras en imagen y sonido, y
                con detalles que permiten afirmar que el espectáculo y el ambiente inundan las butacas,
                donde su modelo de representación en lugares icónicos, como las plazas de toros,
                sumerge al espectador en un viaje a la España de los años 40. Todo ello configura un
                espectáculo audiovisual de primera línea, original e innovador, superando a las
                producciones estadounidenses y europeas que hasta ahora han ostentado el monopolio
                de este sector.
                <br/><br/>
                A nivel técnico artístico, ésta es la primera vez que se fusiona lírica con flamenco
                y danza, y la primera vez que, además, se fusionan las áreas más clásicas con las
                tecnologías más vanguardistas.
                <br/><br/>
            <i class="red-bold">EL ESPECTÁCULO 2.0 COMO VENTAJA COMPETITIVA</i>
            Las nuevas tendencias de consumo en este tipo de espectáculos satisfacen a un
            espectador que pide ser tenido mucho más en cuenta, que demanda no solo un show, sino también un conjunto de
            sensaciones, un producto mucho más elaborado que le
            aporte una emoción mucho más vívida.
            <br/><br/>
            En el caso de Manolete, este aspecto forma parte de su ventaja competitiva,
            aplicándolo a una escala de grandes dimensiones.
            <br/><br/>
            Un nuevo estilo en el que Manolete, precisamente, se posiciona como referente al
            combinar las características más innovadoras del sector (espectáculo 2.0) con las
            especialidades musicales más clásicas, tales como lírica y flamenco, en un espectáculo
            de grandes dimensiones.
            <br/><br/>
            <strong>Existen grandes espectáculos 2.0, pero hasta el momento ninguno de
                    ellos se ha fusionado con éxito con otras habilidades artísticas. Este detalle
                    es el que diferencia y marca un antes y un después, posicionando a
                Manolete como referente del espectáculo 2.0</strong>.

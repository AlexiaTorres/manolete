            <u>Dairley Azevedo</u>
            <br/>
            Estudia en el Conservatorio Estatal de Música, obteniendo Matrícula de Honor y
            Premio como joven promesa.
            <br/><br/>
            En 1998 se traslada a España y complementa sus estudios en la Escuela Superior
            de Canto de Madrid. Fue alumna de la profesora Ángeles Chamorro y obtuvo clases
            magistrales de la mezzosoprano Elena Obrazova, Miguel Zanetti y Ana Luisa Chova.
            <br/><br/>
            Ha intervenido en importantes recitales, destacando los realizados en el
            Parlamento Europeo de Estrasburgo y Bruselas, Embajada de España en Londres
            (Rudolfino), Praga, Auditorio Nacional de Música, de Madrid (con la presencia de los
            Reyes de España), Teatro Real, (como solista de la Novena Sinfonía de Beethoven) y en
            el concierto de reinauguración del Museo Pompidou en Paris, entre otros.
            <br/><br/>
            Otra interpretación suya ha sido el rol de Carmen, en la ópera de Bizet de igual
            nombre.
            <br/><br/>
            Asimismo, tiene una amplia experiencia empresarial y como directora y
            productora artística, ya que en los últimos 15 años, además de crear su propia
            productora, ha dirigido diferentes espectáculos operísticos, entre los que cabe destacar,
            por ejemplo, “Las moradas de Santa Teresa” y “La pasión de la cruz”.
